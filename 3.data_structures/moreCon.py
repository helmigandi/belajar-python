# The conditions used in while and if statements can contain any 
# operators, not just comparisons.

# The comparison operators in and not in check whether a value 
# occurs (does not occur) in a sequence. The operators is and is 
# not compare whether two objects are really the same object; 
# this only matters for mutable objects like lists. 
# All comparison operators have the same priority, which is lower
# than that of all numerical operators.

# Comparisons can be chained. For example, a < b == c tests 
# whether a is less than b and moreover b equals c.